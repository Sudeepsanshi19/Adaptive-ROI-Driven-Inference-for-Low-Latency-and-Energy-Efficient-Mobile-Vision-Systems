print("="*55)

# Collect validation image paths once (plain file list — no Keras generator)
val_img_paths = sorted(
    glob.glob(os.path.join(OUTPUT_DIR, 'val', '*', '*.jpg'))
)
print(f"Calibration images available: {len(val_img_paths)}")

# Shuffle and cap at 200 for calibration speed
import random
random.shuffle(val_img_paths)
calib_paths = val_img_paths[:200]

def representative_dataset_gen():
    """
    Pure-NumPy calibration generator.
    Reads each image from disk, resizes to 224x224,
    applies MobileNetV2 preprocess_input (scale to [-1,1]),
    yields shape (1, 224, 224, 3) float32.
    No Keras generators — zero API compatibility issues.
    """
    preprocess = tf.keras.applications.mobilenet_v2.preprocess_input
    for img_path in calib_paths:
        try:
            img = tf.io.read_file(img_path)
            img = tf.image.decode_jpeg(img, channels=3)
            img = tf.image.resize(img, [224, 224])
            img = tf.cast(img, tf.float32)
            img = preprocess(img)                       # → [-1, 1]
            img = tf.expand_dims(img, axis=0)           # → (1, 224, 224, 3)
            yield [img.numpy()]
        except Exception:
            continue   # skip corrupt images silently


# ── Convert A: Hybrid TFLite (INT8 weights + FP32 activations) ──────────────
print("\nConverting → Hybrid TFLite (INT8 weights, FP32 activations)...")
converter_hybrid = tf.lite.TFLiteConverter.from_keras_model(model_pruned)
