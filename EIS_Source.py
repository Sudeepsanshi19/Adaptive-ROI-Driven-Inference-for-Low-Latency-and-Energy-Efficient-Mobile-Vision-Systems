# NOTE: Do NOT add SELECT_TF_OPS here — it breaks calibration in TF 2.20
converter_hybrid.target_spec.supported_ops = [tf.lite.OpsSet.TFLITE_BUILTINS]
converter_hybrid.representative_dataset = representative_dataset_gen

tflite_hybrid = converter_hybrid.convert()

with open('/content/model.tflite', 'wb') as f:
    f.write(tflite_hybrid)

size_hybrid = os.path.getsize('/content/model.tflite') / 1e6
