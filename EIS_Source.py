# ════════════════════════════════════════════════════════════
# STEP 10: Manual Magnitude Pruning (50% sparsity on Dense layers)
# ════════════════════════════════════════════════════════════
print("\n" + "="*55)
print("STEP 10: 50% Magnitude Pruning on Dense Layers")
print("="*55)

def apply_magnitude_pruning(model, target_sparsity=0.50):
    # Clone the model so we don't modify the trained original
    model_copy = tf.keras.models.clone_model(model)
    model_copy.set_weights(model.get_weights())

    total_pruned = 0
    total_weights = 0

    for layer in model_copy.layers:
        if not isinstance(layer, tf.keras.layers.Dense):
            continue
        weights = layer.get_weights()   # [kernel, bias]
        kernel  = weights[0]

        flat      = np.abs(kernel.flatten())
        k         = int(len(flat) * target_sparsity)
        if k == 0:
            continue
        # Find the k-th smallest absolute value as threshold
        threshold = np.partition(flat, k)[k]
        mask      = np.abs(kernel) >= threshold
        weights[0] = kernel * mask
        layer.set_weights(weights)

        pruned = int(np.sum(mask == 0))
        total_pruned  += pruned
        total_weights += kernel.size
        print(f"  {layer.name:30s}: {pruned:6d}/{kernel.size:6d} zeroed "
              f"({pruned/kernel.size*100:.1f}%)")

    # Overall sparsity across all layers
    az = at = 0
    for layer in model_copy.layers:
        for w in layer.weights:
            a = w.numpy()
            az += int(np.sum(a == 0))
            at += a.size
    print(f"\n  Dense-layer sparsity : {total_pruned/max(total_weights,1)*100:.1f}%  (paper target: 50%)")
    print(f"  Overall sparsity     : {az/at*100:.1f}%")
    return model_copy

model_pruned = apply_magnitude_pruning(model, target_sparsity=0.50)

