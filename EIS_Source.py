"""---
## ✂️ Step 3: Crop 500 Clean ROI Images Per Class

This is the key step. We use COCO's bounding box annotations to **crop each object out** of the full image — exactly like how the Android app extracts ROIs at runtime. This gives the model training data that perfectly matches what it will see during inference.
"""

import json
import cv2
import numpy as np
import random
from PIL import Image
from tqdm import tqdm
import shutil

# ─── CONFIG (Paper Table I) ───────────────────────────────────────────────
TARGET_CLASSES   = ['person', 'bicycle', 'car', 'cat', 'dog',
                    'bottle', 'cell phone', 'backpack']
CLASS_FOLDER_MAP = {cls: cls.replace(' ', '_') for cls in TARGET_CLASSES}
FOLDER_NAMES     = list(CLASS_FOLDER_MAP.values())

IMAGES_PER_CLASS = 500   # exactly as stated in paper Table I
IMG_SIZE         = 224   # MobileNetV2 input
TRAIN_RATIO      = 0.70
VAL_RATIO        = 0.15
# TEST_RATIO = 0.15 (remainder)

MIN_CROP_PX  = 40    # minimum bounding box side length in pixels
MIN_AREA_PX  = 1600  # minimum bbox area (40x40)
SEED         = 42
random.seed(SEED)
np.random.seed(SEED)

OUTPUT_DIR = '/content/dataset'

# Clean any previous run
if os.path.exists(OUTPUT_DIR):
    shutil.rmtree(OUTPUT_DIR)

for split in ['train', 'val', 'test']:
    for folder in FOLDER_NAMES:
        os.makedirs(os.path.join(OUTPUT_DIR, split, folder), exist_ok=True)

print('✅ Output folder structure created')
print(f'Classes : {FOLDER_NAMES}')

# ─── Load COCO train2017 annotations ─────────────────────────────────────
ANNO_PATH = 'annotations/instances_train2017.json'
IMG_DIR   = 'train2017'

print('Loading COCO annotations (this JSON is ~250 MB, takes ~30s)...')
with open(ANNO_PATH) as f:
    coco = json.load(f)

# Build lookup dicts
image_id_to_info = {img['id']: img for img in coco['images']}
cat_id_to_name   = {cat['id']: cat['name'] for cat in coco['categories']}
cat_name_to_id   = {cat['name']: cat['id'] for cat in coco['categories']}

print(f'Total images      : {len(coco["images"]):,}')
print(f'Total annotations : {len(coco["annotations"]):,}')
print(f'Total categories  : {len(coco["categories"])}')

# Check our target classes exist
print('\nTarget class IDs in COCO:')
target_cat_ids = {}
for cls in TARGET_CLASSES:
    if cls in cat_name_to_id:
        cid = cat_name_to_id[cls]
        target_cat_ids[cid] = cls
        print(f'  [{cid:2d}] {cls}')
    else:
        print(f'  [??] {cls} — NOT FOUND in COCO!')

# ─── Group annotations by class ───────────────────────────────────────────
from collections import defaultdict

anns_by_class = defaultdict(list)
for ann in coco['annotations']:
    if ann['category_id'] in target_cat_ids:
        cls = target_cat_ids[ann['category_id']]
        x, y, w, h = ann['bbox']
        # Filter: must be large enough to be a meaningful crop
        if w >= MIN_CROP_PX and h >= MIN_CROP_PX and w * h >= MIN_AREA_PX:
            anns_by_class[cls].append(ann)

print('Annotations available (after size filter):')
for cls in TARGET_CLASSES:
    n = len(anns_by_class[cls])
    bar = '█' * min(50, n // 200)
    print(f'  {cls:12s}: {n:6,}  {bar}')

# ─── Crop & save 500 images per class with 70/15/15 split ────────────────
def crop_and_save(ann, image_info, img_dir, out_path, img_size=224):
    img_path = os.path.join(img_dir, image_info['file_name'])
    img = cv2.imread(img_path)
    if img is None:
        return False

    x, y, w, h = map(int, ann['bbox'])
    ih, iw = img.shape[:2]

    # Add small context padding (10%) around the bbox for better context
    pad_x = int(w * 0.10)
    pad_y = int(h * 0.10)
    x1 = max(0, x - pad_x)
    y1 = max(0, y - pad_y)
    x2 = min(iw, x + w + pad_x)
    y2 = min(ih, y + h + pad_y)

    crop = img[y1:y2, x1:x2]
    if crop.size == 0 or crop.shape[0] < 10 or crop.shape[1] < 10:
        return False

    crop = cv2.resize(crop, (img_size, img_size), interpolation=cv2.INTER_LANCZOS4)
    cv2.imwrite(out_path, crop, [cv2.IMWRITE_JPEG_QUALITY, 93])
    return True


total_saved = 0
final_counts = {split: {} for split in ['train', 'val', 'test']}

for cls in TARGET_CLASSES:
    folder = CLASS_FOLDER_MAP[cls]
    anns   = anns_by_class[cls].copy()
    random.shuffle(anns)

    saved = []
    for ann in tqdm(anns, desc=f'{cls:12s}', leave=False):
        if len(saved) >= IMAGES_PER_CLASS:
            break
        img_info = image_id_to_info[ann['image_id']]
        # Temp path — we'll move to split folders after
        tmp_path = f'/tmp/roi_{cls}_{len(saved)}.jpg'
        if crop_and_save(ann, img_info, IMG_DIR, tmp_path, IMG_SIZE):
            saved.append(tmp_path)

    # Shuffle and split into 70/15/15
    random.shuffle(saved)
    n = len(saved)
    n_train = int(n * TRAIN_RATIO)
    n_val   = int(n * VAL_RATIO)

    split_map = (
        ('train', saved[:n_train]),
        ('val',   saved[n_train:n_train + n_val]),
        ('test',  saved[n_train + n_val:]),
    )

    for split_name, paths in split_map:
        final_counts[split_name][folder] = 0
        for i, src in enumerate(paths):
            dst = os.path.join(OUTPUT_DIR, split_name, folder, f'{i:04d}.jpg')
            shutil.move(src, dst)
            final_counts[split_name][folder] += 1
            total_saved += 1

    print(f'  {cls:12s}: {n} saved  '
          f'(train={final_counts["train"][folder]}, '
          f'val={final_counts["val"][folder]}, '
          f'test={final_counts["test"][folder]})')

print(f'\n✅ Total images saved: {total_saved}')
print(f'   Train : {sum(final_counts["train"].values())}')
print(f'   Val   : {sum(final_counts["val"].values())}')
print(f'   Test  : {sum(final_counts["test"].values())}')

