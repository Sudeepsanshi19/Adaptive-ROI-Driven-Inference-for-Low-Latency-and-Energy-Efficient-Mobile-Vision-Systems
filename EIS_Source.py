def benchmark_tflite(model_path, data_gen, n_batches=10):
    interp = tf.lite.Interpreter(model_path=model_path)
    interp.allocate_tensors()
    inp_idx = interp.get_input_details()[0]['index']
    out_idx = interp.get_output_details()[0]['index']

    data_gen.reset()
    correct = total = 0
    latencies = []

    for i, (imgs, labels) in enumerate(data_gen):
        if i >= n_batches:
            break
        for j in range(len(imgs)):
            x = np.expand_dims(imgs[j], 0).astype(np.float32)
            interp.set_tensor(inp_idx, x)
            t0 = time.perf_counter()
            interp.invoke()
            latencies.append((time.perf_counter() - t0) * 1000)
            pred = np.argmax(interp.get_tensor(out_idx)[0])
            if pred == np.argmax(labels[j]):
                correct += 1
            total += 1

    acc     = correct / total if total > 0 else 0.0
    avg_lat = float(np.mean(latencies)) if latencies else 0.0
    return acc, avg_lat

